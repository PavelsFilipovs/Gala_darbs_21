package Game_21;

public class Card {
	private String cardType;
	private int[] value;
	
	
	public String getCardType() {
		return cardType;
	}
	public void setCardType(String cardType) {
		this.cardType = cardType;
	}
	
	public int[] getValue() {
		return value;
	}
	public void setValue(int[] value) {
		this.value = value;
	}

}
